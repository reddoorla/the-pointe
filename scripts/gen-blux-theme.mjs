// Deterministically turn the Blux export's theme.css (role vars) into a
// consumable stylesheet: the role custom properties under :root, plus one
// `.txt-role-textN` utility per role that maps its vars onto the heading/
// paragraph it wraps. This is the pattern `blux emit` will own — no per-site
// hand-tuning, no LLM tokens. Run: node scripts/gen-blux-theme.mjs <theme.css>
import { readFileSync, writeFileSync } from "node:fs";

const themePath =
  process.argv[2] ||
  "/Users/tuckerlemos/Desktop/thePointe/blux-out-v7/theme.css";
const outPath = process.argv[3] || "src/blux-theme.generated.css";

const css = readFileSync(themePath, "utf8");

// Collect every --text-textN[...] declaration and the roles present.
const roleVars = new Map(); // role -> [ "--text-text5: 15px;", ... ]
const roles = new Set();
for (const m of css.matchAll(
  /(--text-(text\d+)(?:--[a-z-]+)?)\s*:\s*([^;]+);/g,
)) {
  const [, name, role, rawValue] = m;
  let value = rawValue.trim();
  // A multi-word family ("Scope One") is invalid unquoted; quote the first
  // family, leave any fallback stack (comma) alone.
  if (
    name.endsWith("--font-family") &&
    /\s/.test(value) &&
    !/['"]/.test(value)
  ) {
    const [first, ...rest] = value.split(",");
    value = [`"${first.trim()}"`, ...rest.map((s) => s.trim())].join(", ");
  }
  roles.add(role);
  if (!roleVars.has(role)) roleVars.set(role, []);
  roleVars.get(role).push(`  ${name}: ${value};`);
}

const sortedRoles = [...roles].sort(
  (a, b) => Number(a.slice(4)) - Number(b.slice(4)),
);

const lines = [
  "/* GENERATED by scripts/gen-blux-theme.mjs from the Blux export's theme.css.",
  "   Do not edit by hand — re-run the generator (eventually `blux emit`). */",
  "",
  ":root {",
  ...sortedRoles.flatMap((r) => roleVars.get(r)),
  "}",
  "",
];

// One utility per role. Referencing a var the role doesn't define is safe:
// it resolves to the property's initial value (e.g. letter-spacing: normal).
for (const r of sortedRoles) {
  lines.push(
    `.txt-role-${r} :is(h1, h2, h3, h4, h5, h6, p) {`,
    `  font-family: var(--text-${r}--font-family, var(--font-heading));`,
    `  font-size: var(--text-${r});`,
    `  font-weight: var(--text-${r}--font-weight);`,
    `  line-height: var(--text-${r}--line-height);`,
    `  letter-spacing: var(--text-${r}--letter-spacing, normal);`,
    `  text-transform: var(--text-${r}--text-transform, none);`,
    `  margin: 0;`,
    `}`,
    "",
  );
}

writeFileSync(outPath, lines.join("\n"));
console.log(
  `wrote ${outPath}: ${sortedRoles.length} roles (${sortedRoles.join(", ")})`,
);
